class RectangleAreaPerimeter 
{
	public static void main(String[] args) 
	{
		float width = 4.5f;
		float height = 7.9f;

		float area = width * height;

		float perimeter = 2*width + 2*height;

		System.out.println("Area : " + area);
		System.out.println("Perimeter : " + perimeter);
	}
}
